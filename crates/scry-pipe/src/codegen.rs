    #[test]
    fn engine_codegen_parity_simple() {
        // Verify that the engine and generated code produce identical
        // results for the proposal pipeline.
        use crate::engine::PipelineEngine;

        let def = proposal_pipeline();
        let engine = PipelineEngine::new(def.clone());

        // Test input: age=40, income=100_000, city=1 (NYC)
        let input = vec![40.0, 100_000.0, 1.0];
        let engine_out = engine.transform_row(&input).unwrap();

        // Now compute what the generated code should produce:
        // Step 0: (40 - 35.2) / 12.1
        let step0: f64 = (40.0 - 35.2) / 12.1;
        // Step 1: (100_000 - 20_000) / 480_000
        let step1: f64 = (100_000.0 - 20_000.0) / 480_000.0;
        // Step 2: passthrough = 1.0
        let step2: f64 = 1.0;
        // Step 3: clip step0 to [0, 120]
        let step3 = step0.clamp(0.0, 120.0);
        // Step 4: ln_1p(step1)
        let step4 = step1.ln_1p();

        // The engine's output should match.
        // Note: engine produces 5 values because clip and log1p create
        // new output slots in the engine. Let me check the engine output dim.
        //
        // Actually, re-reading engine code: the engine modifies the row
        // in-place. Clip on feature_idx=0 modifies row[0], Log1p on
        // feature_idx=1 modifies row[1]. So the engine output is 3 values.
        assert_eq!(engine_out.len(), 3);
        assert!((engine_out[0] - step3).abs() < 1e-10, "age after scale+clip");
        assert!((engine_out[1] - step4).abs() < 1e-10, "income after scale+log1p");
        assert!((engine_out[2] - step2).abs() < 1e-10, "city label");
    }
